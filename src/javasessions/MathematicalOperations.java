package javasessions;

public class MathematicalOperations {

	public static void main(String[] args) {
		
		System.out.println(10/2);//integer
		System.out.println(20/2);// integer
		//System.out.println(20/0); //Arithmetic exception
		
		System.out.println(20/0.0);// infinity
		System.out.println(2.0/0); //infinity
		System.out.println(0/3); //0
		System.out.println(0/0.0);// NaN not a number
		System.out.println(0.0/0);// NaN not a number
		System.out.println(10/2.0);// float
		System.out.println(10.0/2);// float
}
}
